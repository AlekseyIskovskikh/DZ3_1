public class health {
private double weight;
private int step;

    public health(double weight, int step) {
        this.weight = weight;
        this.step = step;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public int getStep() {
        return step;
    }

    public void setStep(int step) {
        this.step = step;
    }
}
